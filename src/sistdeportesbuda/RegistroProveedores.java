    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        
        Connection con = null;
        try{
        con = getConnection();    
        ps = con.prepareStatement("INSERT INTO proveedor (codigo, producto, nombre, telefono, email, direccion) VALUES(?, ?, ?, ?,?,?)");
        
        ps.setString(1, txtCodigo.getText());
        ps.setString(2, txtProducto.getText());
        ps.setString(3, txtNombre.getText());
        ps.setString(4, txtTelefono.getText());
        ps.setString(5, txtEmail.getText());
        ps.setString(6, txtDireccion.getText());
        
        int res = ps.executeUpdate();
        
        if(res>0){
          JOptionPane.showMessageDialog(null, "Registro Guardado");
          Limpiar();
        }else{
           JOptionPane.showMessageDialog(null, "Error al Guardar");
           Limpiar();
        }
        
        con.close();
        
        }catch(Exception e){
        
            JOptionPane.showMessageDialog(null, "Codigo de Proveedor ya Existe");
    }
         
         
        
    }//GEN-LAST:event_btnregistrarActionPerformed

