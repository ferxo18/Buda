    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        Properties props = new Properties();// creo las propiedades para la conexion con gmail
        props.setProperty("mail.smtp.host", "smtp.gmail.com");
        props.setProperty("mail.smtp.starttls.enable", "true");
        props.setProperty("mail.smtp.port", "587");
        props.setProperty("mail.smtp.auth", "true");
        
       Session session = Session.getDefaultInstance(props);// envio las propiedades
       
       String Remitente = "jflopez3085@gmail.com";// correo del cual se enviaran los datos y declaro variables que almacenen el cuerpo del mail
       String clave = "jlmm300213";
       String Receptor = txtMaildestino.getText();
       String Asunto = txtAsunto.getText();
       String Mensaje = txaMensaje.getText();
       
       MimeMessage message = new MimeMessage(session);// se envia inicio de Sesion
       
       message.setFrom(new InternetAddress(Remitente));// se crea el correo remitente
       message.addRecipient(Message.RecipientType.TO, new InternetAddress(Receptor));// se agrega correo receptor
       message.setSubject(Asunto);
       message.setText(Mensaje);
       
       Transport t = session.getTransport("smtp");// quue tipo de envio y mando cuenta del remitente, clave y Mail receptor
       t.connect(Remitente, clave);
       t.sendMessage(message, message.getRecipients(Message.RecipientType.TO));
       t.close();
       
       JOptionPane.showMessageDialog(null, "Pedido Enviado");
       
        }catch(AddressException ex){
            
            JOptionPane.showMessageDialog(null, ex);
            
        } catch (MessagingException ex) {
            Logger.getLogger(RealizarCompra.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

